                            			<p class="py-3 text" style="line-height: 30px;letter-spacing: 0.5px;">WordPress is a great software to build websites and blogs and publish it online. WordPress is the first thing when it comes to people’s mind to create a website or a blog or an e-commerce website. In WordPress Software, websites can also be created by those people who don’t have any knowledge regarding coding, thus it’s so  simple to use wordpress. You can download it freely.</p>

                                        <p style="line-height: 30px;letter-spacing: 0.5px;">
                                            Anyone can make the website without any coding experience who wants to build the blogs and websites. Any person can install it, modify and use the wordpress.  WordPress is the first thing that comes to people’s mind when you have to build your website with wordpress. WordPress builds the website within an hour once you purchase the domain name and hosting.
                                        </p>


                                        <p style="line-height: 30px;letter-spacing: 0.5px;">
                                            At present there is a lack of  professional websites that are made on wordpress. In the year 2003, a twenty year college student Matt Mullenweg launched the wordpress. At that time he also didn’t know that this wordpress software in the future was going to be used by lakhs of people.
                                        </p>

                                        <p style="line-height: 30px;letter-spacing: 0.5px;">
                                            It’s not necessary to have the knowledge of web designing to make a professional website. There are tools where you can make the website by purchasing domain name and hosting. When it comes to tools for making websites, WordPress is the number one tool for making websites.
                                        </p>

                                        <img src="images/blog/used_wordpress.webp" alt="" class="img-fluid">

                                        <h3>What things WordPress is used for?</h3>

                                        <p style="line-height: 30px;letter-spacing: 0.5px;">
                                            WordPress is a “Content Management System” or “CMS” where you can earn, add images, Videos, Graphics and display it on a website. In the initial stage wordpress was only used for blogging, but gradually software has developed rapidly. You can create whatever website you want by using this software. You can create a Personal blog, Business Website, Professional Portfolio website. You can make any website according to your own needs.
                                        </p>

                                        <h3>How can you start with the wordpress?</h3>
                                        <p class="py-3" style="line-height: 30px;letter-spacing: 0.5px;">WordPress is totally free to start your website and blogging. There are two types of Categories in wordpress</p>

                                        <p style="line-height: 30px;letter-spacing: 0.5px;">1. WordPress.org:  You have to incur expenses to purchase hosting and domain name. There are various wordpress hosting sites available. There are also companies that are providing host free wordpress websites for the first year.</p>

                                        <p style="line-height: 30px;letter-spacing: 0.5px;">2. WordPress.com:  If you cannot afford to invest your own money in purchasing hosting and domain name then you can make your website in “WordPress.com”. But the only problem is Your name won’t be registered completely in wordpress. After the name, wordpress.com would be there. Hence, you can build a free wordpress website.</p>

                                        <p style="line-height: 30px;letter-spacing: 0.5px;">
                                            So, What are domain name and hosting?
                                        </p>

                                        <p style="line-height: 30px;letter-spacing: 0.5px;">
                                            Domain is nothing but the name of your website. Hosting is where all your website’s files  are stored. Why is it necessary to purchase the domain? Once you purchase the domain name, no one can copy your website name until it expires. Every year you have to pay the domain fees. Fees depend on the domain. Approximately it costs Rs. 500-1000.
                                        </p>

                                        <h3>How to make a website in wordpress?</h3>
                                        <h4>1. Sign up</h4>
                                        <p style="line-height: 30px;letter-spacing: 0.5px;">
                                            First thing which you have to do is get signed up on the wordpress website. You have to give your user account and password. As I described before that wordpress gives you two options i.e. 
                                        </p>

                                        <h6>WordPress.com</h6>
                                        <p style="line-height: 30px;letter-spacing: 0.5px;">
                                            It is free to use but the only one thing is at the end of the website name wordpress.com is placed. The name is not registered entirely on your name. The link of the website is http://www.yourwebsitename.wordpress.com/
                                        </p>

                                        <h6>WordPress.org</h6>
                                        <p style="line-height: 30px;letter-spacing: 0.5px;">
                                            You have to incur expenses for purchasing your domain name and hosting. The domain name will be registered on your name and no one can copy it, this is the speciality of wordpress.org. But the only difference is wordpress.com won’t be there after the website name. Every year you have to pay the fees of hosting. The link of the website is http://www.yourwebsitename.com/
                                        </p>

                                        <h3>2. Dashboard</h3>
                                        <p style="line-height: 30px;letter-spacing: 0.5px;">Once you purchase the domain name and hosting. Website name has been selected, have a look in the dashboard. This is the place where you will do all the work to keep your website professionally  up-to-date.</p>

                                        <h3>3. Selecting a Theme</h3>
                                        <p style="line-height: 30px;letter-spacing: 0.5px;">
                                            It is very important to choose a theme, the theme is nothing but a design. You can add your own images and you can change the designs according to your wish and choice. In the theme you can add custom images, fonts, adding titles, headers and colours.
                                        </p>

                                        <h3>4. Create Your First Page</h3>
                                        <p style="line-height: 30px;letter-spacing: 0.5px;">
                                            After selecting a theme, you can start writing. Therefore, this creates the first page of your website after you write the content. This is how you can make a website or a blog from wordpress software in a simple and an easy manner.
                                        </p>

                                        <h3>5. Plugins</h3>
                                        <p style="line-height: 30px;letter-spacing: 0.5px;">
                                            Plugins means adding new features. You can download any feature which you want for your website like how you download the apps in your mobile phone.
                                        </p>

                                        <h3>6. Marketing</h3>
                                        <p style="line-height: 30px;letter-spacing: 0.5px;">
                                            Don’t forget to do the marketing for your website. Unless and until you won’t promote your website or blogging, how will people come to know about your website? Hence, marketing is too important. You can do the marketing through Social Media, Email Marketing, by youtube videos.
                                        </p>

                                        <h3>Takeaway</h3>
                                        <p style="line-height: 30px;letter-spacing: 0.5px;">1. You just need to understand what your niche is. Is it for  business, blogging or for a hobby- you want to make a website.</p>
                                        <p style="line-height: 30px;letter-spacing: 0.5px;">2. Choose an appropriate website name.</p>
                                        <p style="line-height: 30px;letter-spacing: 0.5px;">3. Understand what is domain name and hosting. Understand the meaning of both.</p>
                                        <p style="line-height: 30px;letter-spacing: 0.5px;">4. Select the hosting, from where you want to purchase. Do a complete research on it.</p>
                                        <p style="line-height: 30px;letter-spacing: 0.5px;">5. In wordpress there are two options available whether you want to make your website free or want to go for paid.</p>
                                        <p style="line-height: 30px;letter-spacing: 0.5px;">6. After purchasing the domain and hosting, install a wordpress.</p>
                                        <p style="line-height: 30px;letter-spacing: 0.5px;">7. Later, select a theme for your website</p>
                                        <p style="line-height: 30px;letter-spacing: 0.5px;">8. Write the content according to your niche.</p>
                                        <p style="line-height: 30px;letter-spacing: 0.5px;">Hece, your first page is ready.</p>
                                

                                       
                            		                            	
                            		
